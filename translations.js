function toggleLanguage() {
    const currentLang = document.documentElement.lang;
    const newLang = currentLang === 'es' ? 'en' : 'es';
    document.documentElement.lang = newLang;
    
    // Actualizar las traducciones
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[newLang][key]) {
            if (element.tagName === 'INPUT') {
                element.placeholder = translations[newLang][key];
            } else {
                element.textContent = translations[newLang][key];
            }
        }
    });

    // Actualizar el icono y texto del botón de idioma
    const languageIcon = document.getElementById('languageIcon');
    const languageText = document.getElementById('languageText');
    
    if (newLang === 'es') {
        languageIcon.src = 'images/espana.png';
        languageText.textContent = 'ES';
    } else {
        languageIcon.src = 'images/inglaterra.png';
        languageText.textContent = 'EN';
    }
}

